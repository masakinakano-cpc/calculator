/**
 * 指定した日付の雑学を取得（年ごとに情報を更新）
 * @param dateStr 日付文字列 (YYYY-MM-DD)
 * @returns 雑学の配列（記念日と歴史的出来事を含む）
 */
export function getDateTrivia(dateStr: string): string[] {
  const date = new Date(dateStr);
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  const key = `${month}-${day}`;
  const currentYear = new Date().getFullYear();

  const trivia: string[] = [];

  // 記念日を追加
  if (DATE_TRIVIA[key]) {
    trivia.push(...DATE_TRIVIA[key]);
  }

  // 歴史的出来事を追加
  if (HISTORICAL_EVENTS[key]) {
    trivia.push(...HISTORICAL_EVENTS[key]);
  }

  // 年ごとの情報を追加（今年の場合は特別表示）
  if (year === currentYear) {
    trivia.push(`${year}年 今年の今日`);
  } else if (year < currentYear) {
    const yearsAgo = currentYear - year;
    if (yearsAgo === 1) {
      trivia.push(`${year}年 1年前の今日`);
    } else if (yearsAgo === 5) {
      trivia.push(`${year}年 5年前の今日`);
    } else if (yearsAgo === 10) {
      trivia.push(`${year}年 10年前の今日`);
    } else if (yearsAgo === 25) {
      trivia.push(`${year}年 25年前の今日`);
    } else if (yearsAgo === 50) {
      trivia.push(`${year}年 50年前の今日`);
    } else if (yearsAgo === 100) {
      trivia.push(`${year}年 100年前の今日`);
    }
  }

  // もし何もない場合は、一般的な情報を追加
  if (trivia.length === 0) {
    trivia.push(`${month}月${day}日の特別な日`);
  }

  return trivia;
}

/**
 * 今日の雑学を取得
 * @returns 雑学の配列
 */
export function getTodayTrivia(): string[] {
  const today = new Date();
  const year = today.getFullYear();
  const month = String(today.getMonth() + 1).padStart(2, '0');
  const day = String(today.getDate()).padStart(2, '0');
  return getDateTrivia(`${year}-${month}-${day}`);
}
